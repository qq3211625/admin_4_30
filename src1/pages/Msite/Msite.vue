<script>
  import Swiper from 'swiper'
  import ShopList from '../../conponents/ShopList/ShopList'
  import 'swiper/dist/css/swiper.css'
  import {mapState} from 'vuex'

    export default {
      mounted() {
        if(this.categorys.length!=0){
          new Swiper('.swiper-container', {
            loop: true, // 循环模式选项
            // 如果需要分页器
            pagination: {
              el: '.swiper-pagination',
            },
          })
        }
      },
      components: {
        ShopList
      },
      computed: {
        //提取state的值
        ...mapState({
          categorys: state => state.msite.categorys
        }),

        //根据categorys生成二维数组
        categorysList () {
          let bigArr = []
          let smallArr = []
          //遍历一维数组
          this.categorys.forEach( (value, index) => {

              if(smallArr.length===0){
                bigArr.push(smallArr);
              }

              smallArr.push(value)
              if(smallArr.length===8) {
                smallArr=[]
              }
          })
          return bigArr
        }
      },
      watch: {
        //状态数据变化后:立即同步执行监事的回调,异步更新界面
        categorys (value) { //categorys此时拿去数据了
          //创建时机,在数据页面显示之后
          this.$nextTick(() => {
            new Swiper('.swiper-container', {
              loop: true, // 循环模式选项
              // 如果需要分页器
              pagination: {
                el: '.swiper-pagination',
              },
            })
          })
        }
      },
    }
</script>



